"use client";

import React, { useState, useEffect } from "react";
import Link from "next/link";
import Image from "next/image";
import { ArrowLeft, MessageCircle, Send, Loader2 } from "lucide-react";
import { cn } from "@/lib/utils";

interface SupportConfig {
  whatsappNumber: string | null;
  whatsappEnabled: boolean;
  telegramUser: string | null;
  telegramEnabled: boolean;
}

export default function AjudaPage() {
  const [isLoading, setIsLoading] = useState(true);
  const [config, setConfig] = useState<SupportConfig>({
    whatsappNumber: null,
    whatsappEnabled: false,
    telegramUser: null,
    telegramEnabled: false,
  });

  useEffect(() => {
    loadConfig();
  }, []);

  const loadConfig = async () => {
    try {
      const res = await fetch("/api/support/config");
      const data = await res.json();
      
      if (data.success) {
        setConfig(data.config);
      }
    } catch (error) {
      console.error("Erro ao carregar configurações:", error);
    } finally {
      setIsLoading(false);
    }
  };

  const openWhatsApp = () => {
    if (config.whatsappNumber) {
      const number = config.whatsappNumber.replace(/\D/g, "");
      window.open(`https://wa.me/${number}`, "_blank");
    }
  };

  const openTelegram = () => {
    if (config.telegramUser) {
      window.open(`https://t.me/${config.telegramUser}`, "_blank");
    }
  };

  if (isLoading) {
    return (
      <div className="min-h-screen bg-[#0a1628] flex items-center justify-center">
        <Loader2 className="w-8 h-8 animate-spin text-[#00faff]" />
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-[#0a1628] pb-24">
      {/* Header */}
      <header className="flex items-center gap-4 p-4 border-b border-white/10">
        <Link href="/perfil" className="text-white/70 hover:text-white transition">
          <ArrowLeft className="w-6 h-6" />
        </Link>
        <h1 className="text-lg font-semibold text-white">Central de Ajuda</h1>
      </header>

      <div className="p-4">
        {/* Título */}
        <div className="text-center mb-8">
          <h2 className="text-xl font-bold text-white mb-2">Contatar Suporte</h2>
          <p className="text-white/60">
            Escolha um canal para falar com nossa equipe
          </p>
        </div>

        {/* Cards de Suporte */}
        <div className="grid grid-cols-2 gap-4">
          {/* WhatsApp */}
          {config.whatsappEnabled && config.whatsappNumber && (
            <div className="bg-white/5 border border-white/10 rounded-2xl p-6 flex flex-col items-center">
              <div className="w-16 h-16 rounded-full bg-green-500/20 flex items-center justify-center mb-4">
                <svg viewBox="0 0 24 24" className="w-8 h-8 text-green-500" fill="currentColor">
                  <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/>
                </svg>
              </div>
              <h3 className="text-white font-medium mb-1">Suporte WhatsApp</h3>
              <p className="text-white/40 text-sm text-center mb-4">Atendimento rápido</p>
              <button
                onClick={openWhatsApp}
                className={cn(
                  "w-full py-2.5 rounded-lg font-medium text-sm transition-all",
                  "bg-green-500 hover:bg-green-600 text-white"
                )}
              >
                Chat
              </button>
            </div>
          )}

          {/* Telegram */}
          {config.telegramEnabled && config.telegramUser && (
            <div className="bg-white/5 border border-white/10 rounded-2xl p-6 flex flex-col items-center">
              <div className="w-16 h-16 rounded-full bg-blue-500/20 flex items-center justify-center mb-4">
                <Send className="w-8 h-8 text-blue-500" />
              </div>
              <h3 className="text-white font-medium mb-1">Suporte Telegram</h3>
              <p className="text-white/40 text-sm text-center mb-4">@{config.telegramUser}</p>
              <button
                onClick={openTelegram}
                className={cn(
                  "w-full py-2.5 rounded-lg font-medium text-sm transition-all",
                  "bg-blue-500 hover:bg-blue-600 text-white"
                )}
              >
                Chat
              </button>
            </div>
          )}
        </div>

        {/* Mensagem se nenhum canal disponível */}
        {!config.whatsappEnabled && !config.telegramEnabled && (
          <div className="text-center py-12">
            <div className="w-20 h-20 rounded-full bg-white/5 flex items-center justify-center mx-auto mb-4">
              <MessageCircle className="w-10 h-10 text-white/20" />
            </div>
            <h3 className="text-white/60 text-lg font-medium mb-2">Suporte indisponível</h3>
            <p className="text-white/40 text-sm">
              Os canais de suporte estão sendo configurados.
            </p>
          </div>
        )}

        {/* Texto informativo */}
        <div className="mt-8 p-4 bg-white/5 rounded-xl border border-white/10">
          <p className="text-white/60 text-sm text-center">
            Bem-vindo! Clique em &quot;Chat&quot; para entrar em contato conosco através do 
            portal de atendimento ao cliente.
          </p>
        </div>
      </div>
    </div>
  );
}
